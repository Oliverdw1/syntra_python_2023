nummers = [1, 2, 3, 4, 5]
namen = ["Oliver", "Inias", "Daan", "Xander", "Robbe"]

print(f"Het eerste nummer uit de lijst is {nummers[0]}")
print(f"De eerste naam uit de lijst is {namen[0]}")

for i in range(1, 5):
    print(f"Het volgende nummer uit de lijst is {nummers[i]}")
    print(f"De volgende naam uit de lijst is {namen[i]}")

